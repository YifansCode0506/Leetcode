/**
 * @param {number[]} citations
 * @return {number}
 */
var hIndex = function(citations) {
    // 在区间 [left, right] 内询问
    const n = citations.length;
    let left = 1;
    let right = n;
    while (left <= right) { // 区间不为空
        // 循环不变量：
        // left-1 的回答一定为「是」
        // right+1 的回答一定为「否」
        const mid = Math.floor((left + right) / 2);
        // 引用次数最多的 mid 篇论文，引用次数均 >= mid
        if (citations[n - mid] >= mid) {
            left = mid + 1; // 询问范围缩小到 [mid+1, right]
        } else {
            right = mid - 1; // 询问范围缩小到 [left, mid-1]
        }
    }
    // 循环结束后 right 等于 left-1，回答一定为「是」
    // 根据循环不变量，right 现在是最大的回答为「是」的数
    return right;
};

// 第一步：把数组想象成一排“台阶”
// 假设一个学者的论文被引用次数是：citations = [0, 1, 3, 5, 6]。数组长度 n = 5（总共 5 篇论文）。

// 因为数组是升序排列的，我们可以把它们画成一排从低到高的台阶：
// 现在，我们把注意力集中在索引 i = 2 的那篇论文上（引用次数是 3）。
// 请问：比这篇论文引用次数“更多或一样多”的论文，在图中的哪里？

// 因为是升序，所以它右边的所有论文，引用次数都一定大于等于它！
// •	包含它自己在内，从索引 2 到最后的索引 4，一共有多少篇论文？
// •	算式就是：$\text{总篇数 } n - \text{当前索引 } i = 5 - 2 = 3$ 篇。

// 第一个顿悟点：只要数组排好序，当你站在索引 i 时，你根本不需要去数数，右边（含自己）的论文篇数铁定是 n - i。并且，这 n - i 篇论文的引用次数，最低最低也有 citations[i] 那么多。


// 第二步：严丝合缝地对齐“H指数的定义”
// 再来回看 H 指数的定义：有 $h$ 篇论文，分别被引用了至少 $h$ 次。

// 我们把刚刚在索引 i = 2 发现的秘密代入进去：
// 1.	我们手头上现在有几篇论文？答：有 n - i = 3 篇论文。
// 2.	这 3 篇论文的引用次数至少是多少？答：至少是 citations[2] = 3 次。

// 现在把这两个数字连起来读一遍：

// “我们有 3 篇论文，分别被引用了至少 3 次。”
// 天哪！这不就完美符合了 $h = 3$ 的定义吗？


// 第三步：合格 vs 不合格的“天平”
// 那是不是任何一个索引 i 算出来的 n - i 都可以直接当成 H 指数呢？不是的。因为这里有一个阈值拦截。

// 再来看索引 i = 1 的那篇论文（引用次数是 1）：
// •	此时从它往后数，一共有 $5 - 1 = 4$ 篇论文（也就是 n - i = 4）。
// •	这 4 篇论文的引用次数，最低最低是 citations[1] = 1 次。
// •	我们连起来读一遍：“我们有 4 篇论文，分别被引用了至少 1 次。”

// 你能说这位学者的 H 指数是 4 吗？不能！ 因为 H 指数要求“有 $h$ 篇论文被引用了至少 $h$ 次”。你虽然凑够了 4 篇论文，但它们的引用次数太低了（只保证了至少 1 次，没能达到 4 次）。

// 所以，这就推导出了核心的判定天平（Check 条件）：

// $$\text{当前这台阶的引用次数 } (\text{citations}[mid]) \ge \text{后面所有的论文篇数 } (n - mid)$$

// •	如果成立（如 citations[2] = 3 >= 3）：说明这 n - mid 篇论文的底气很足（引用次数够大），可以作为 H 指数的候选。
// •	如果不成立（如 citations[1] = 1 < 4）：说明虽然论文篇数看起来很多，但引用次数太拉胯了，撑不起这么大的 H 指数。


// 总结：二分查找到底在找什么？
// 在这个数组里，随着索引 i 从左往右走：
// •	论文的引用次数 citations[i] 是越来越大的（台阶越来越高）。
// •	剩下的论文篇数 n - i 是越来越小的（右边的空间越来越小）。

// 这道题的本质，就是要在数组里找引用次数（台阶高度）和论文篇数（右侧剩余空间）的黄金交叉点！